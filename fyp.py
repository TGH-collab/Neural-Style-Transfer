def load_and_resize_image(path: str, max_dim: int) -> tf.Tensor:
    """Load an image and resize preserving aspect ratio. Returns float tensor [1,H,W,3]."""
    img = Image.open(path).convert("RGB")
    w, h = img.size
    scale = max_dim / max(w, h)
    new_w, new_h = int(w * scale), int(h * scale)
    img = img.resize((new_w, new_h))

    arr = np.array(img).astype(np.float32)
    return tf.convert_to_tensor(arr)[tf.newaxis, ...]

def show_image(t: tf.Tensor, title: str = "") -> None:
    img = tf.squeeze(t, axis=0)
    img = tf.clip_by_value(img, 0.0, 255.0)
    img = tf.cast(img, tf.uint8).numpy()

    plt.figure(figsize=(6, 6))
    if title:
        plt.title(title)
    plt.imshow(img)
    plt.axis("off")
    plt.show()

def save_tensor_as_image(t: tf.Tensor, filename: str) -> None:
    img = tf.squeeze(t, axis=0)
    img = tf.clip_by_value(img, 0.0, 255.0)
    img = tf.cast(img, tf.uint8).numpy()
    Image.fromarray(img).save(filename)

