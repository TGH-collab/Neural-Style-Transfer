def build_vgg_feature_extractor(layer_names):
    """Create a frozen VGG19 model that outputs selected intermediate layer activations."""
    vgg = tf.keras.applications.VGG19(include_top=False, weights="imagenet")
    vgg.trainable = False
    outputs = [vgg.get_layer(n).output for n in layer_names]
    return tf.keras.Model(inputs=vgg.input, outputs=outputs)

def gram_matrix(feature_maps: tf.Tensor) -> tf.Tensor:
    """Compute Gram matrix for style representation. feature_maps: [1,H,W,C] -> [1,C,C]."""
    result = tf.linalg.einsum("bijc,bijd->bcd", feature_maps, feature_maps)
    h = tf.shape(feature_maps)[1]
    w = tf.shape(feature_maps)[2]
    num_locations = tf.cast(h * w, tf.float32)
    return result / num_locations

class NSTFeatureModel(tf.keras.Model):
    """Extract content and style features from a frozen VGG network."""
    def __init__(self, style_layers, content_layers):
        super().__init__()
        self.style_layers = list(style_layers)
        self.content_layers = list(content_layers)
        self.all_layers = self.style_layers + self.content_layers

        self.vgg = build_vgg_feature_extractor(self.all_layers)
        self.vgg.trainable = False
        self.num_style = len(self.style_layers)

    def call(self, x: tf.Tensor):
        x = tf.keras.applications.vgg19.preprocess_input(x)
        outputs = self.vgg(x)

        style_out = outputs[:self.num_style]
        content_out = outputs[self.num_style:]

        style_features = {n: gram_matrix(v) for n, v in zip(self.style_layers, style_out)}
        content_features = {n: v for n, v in zip(self.content_layers, content_out)}
        return style_features, content_features

# =========================
# Loss functions
# =========================
def compute_content_loss(gen_content, target_content) -> tf.Tensor:
    losses = [tf.reduce_mean((gen_content[k] - target_content[k]) ** 2) for k in target_content.keys()]
    return tf.add_n(losses)

def compute_style_loss(gen_style, target_style) -> tf.Tensor:
    losses = [tf.reduce_mean((gen_style[k] - target_style[k]) ** 2) for k in target_style.keys()]
    return tf.add_n(losses)

def total_variation_regulariser(image: tf.Tensor) -> tf.Tensor:
    return tf.reduce_sum(tf.image.total_variation(image))

def total_loss(image, gen_style, gen_content, style_target, content_target, cfg: NSTConfig) -> tf.Tensor:
    style_l = compute_style_loss(gen_style, style_target) / len(cfg.style_layers)
    content_l = compute_content_loss(gen_content, content_target) / len(cfg.content_layers)
    tv_l = total_variation_regulariser(image)

    return (cfg.style_weight * style_l) + (cfg.content_weight * content_l) + (cfg.tv_weight * tv_l)

# =========================
# Optimisation step (IMPORTANT FIX)
# - Do NOT pass cfg into tf.function
# - Use global CFG inside to avoid TraceType error
# =========================
@tf.function
def optimisation_step(image: tf.Variable,
                      model: NSTFeatureModel,
                      style_target,
                      content_target,
                      optimiser):
    with tf.GradientTape() as tape:
        gen_style, gen_content = model(image)
        loss = total_loss(image, gen_style, gen_content, style_target, content_target, CFG)

    grads = tape.gradient(loss, image)
    optimiser.apply_gradients([(grads, image)])
    image.assign(tf.clip_by_value(image, 0.0, 255.0))
    return loss

